package com.dds.pathfinder.itaxiinterface.webservice;


import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "LoginReqType", namespace="http://com.dds.osp.itaxi.interface/", 
			propOrder = { "userName", "password"})
public class LoginRequest extends BaseReq{
	@XmlElement(name="userName", required = true, defaultValue="")
	private String userName;
	
	@XmlElement(name="password", required = true, defaultValue="")
	private String password;

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
}